

#include <stdio.h>
//unaryoperators

int main()

{
    int a=2;
    printf(" Given a=2 \n");

    printf("a++ = %d\n",a++);
    printf("a-- = %d\n",a--);
    printf("++a = %d\n",++a);
    printf("--a = %d\n",--a);
    printf("not a is %d\n",!a);
    printf("size of a is %d\n",sizeof(a));
    printf("Address of a is %d \n",&a);
    
}
